num = input('Enter Day Number:(1~7) ')

if num == '7':
    print('Its Saturday')
elif num == '1':
    print("Its Sunday")
elif num == '2':
    print("Its Monday")
elif num == '3':
    print("Its Tuesday")
elif num == '4':
    print("Its Wednesday")
elif num == '5':
    print("Its Thursday")
elif num == '6':
    print("Its Friday")
else:
    print("Oh NO!")# اعداد روز رو دریافت کنه اسم روز معادل را چاپ کند
