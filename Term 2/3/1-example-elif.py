# '7' => Saturday
# '1' => Sunday
# '2' => Monday
# '3' => Tuesday
# '4' => Wednesday
# '5' => Thursday
# '6' => Friday

num = input('Enter Day Number: ')

if num == '7':
    print('Its Saturday')
elif num == '1':
    print("Its Sunday")
elif num == '2':
    print("Its Monday")
elif num == '3':
    print("Its Tuesday")
elif num == '4':
    print("Its Wednesday")
elif num == '5':
    print("Its Thursday")
elif num == '6':
    print("Its Friday")
